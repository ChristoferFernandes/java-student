package src.exercicios.aula1617;

public class exercicio06 {
    public static void main(String[] args) {

            int count = 1;
            int cont = 1;

            //Programa um abaixo do outro.
        while (count <= 20) {
            System.out.println(count);
            count++;
        }
            //Programa um ao lado do outro.
        while (cont <= 20) {
            System.out.print(" "+ cont);
            cont++;
        }
    }
}
